function Soma()
{   
    var n = parseInt(prompt("Digite o primeiro número: "));
    var n2 = parseInt(prompt("Digite o segundo número: "));
    alert("Resultado: " + (n+n2))
}

function Subtracao()
{
    var n = parseInt(prompt("Digite o primeiro número: "));
    var n2 = parseInt(prompt("Digite o segundo número: "));
    alert("Resultado: " + (n-n2))
}

function Divisao()
{
    var n = parseInt(prompt("Digite o primeiro número: "));
    var n2 = parseInt(prompt("Digite o segundo número: "));
    alert("Resultado: " + (n/n2))
}

function Multiplicacao()
{
    var n = parseInt(prompt("Digite o primeiro número: "));
    var n2 = parseInt(prompt("Digite o segundo número: "));
    alert("Resultado: " + (n*n2))
}